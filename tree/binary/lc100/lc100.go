package lc100

/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

type TreeNode struct {
	Val   int
	Left  *TreeNode
	Right *TreeNode
}

// 100. 相同的树
func isSameTree(p *TreeNode, q *TreeNode) bool {
	// case1
	if p == nil && q == nil {
		return true
	}
	// case2 节点不统一
	if p == nil || q == nil {
		return false
	}
	// case3 节点值不统一
	if p.Val != q.Val {
		return false
	}
	return isSameTree(p.Left, q.Left) && isSameTree(p.Right, q.Right)
}
